# Faça um programa que leia um número interio e mostro seu sucessor e antecessor
n1 = int(input('Dígite um valor: '))
s = n1 + 1
a = n1 - 1
print('Valor digitado: {}. \nSeu sucessor: {}. \nSeu antecessor: {}.'.format(n1, s, a))

